//-----------------------------------------------------------------------------
// カメラクラス
//-----------------------------------------------------------------------------
#include "Camera.h"
#include "Define.h"

//-----------------------------------------------------------------------------
// コンストラクタ
//-----------------------------------------------------------------------------
Camera::Camera()
{
	//カメラの描画範囲を設定する
	SetCameraNearFar(10.0f, 2800.0f);

	m_pos = VGet(0, 0, -120);
}

//-----------------------------------------------------------------------------
// デストラクタ
//-----------------------------------------------------------------------------
Camera::~Camera()
{
	// 処理なし.
}

//-----------------------------------------------------------------------------
// 更新（タイトル）
//-----------------------------------------------------------------------------
void Camera::TitleUpdate()
{
	m_pos = VGet(90.0f, 20.0f, -1000.0f);

	SetCameraPositionAndTarget_UpVecY(m_pos, VGet(0, 400, 0));
}

//-----------------------------------------------------------------------------
// 更新（プレイ中）
//-----------------------------------------------------------------------------
void Camera::Update()
{
	m_pos = VGet(CAMERA_X, CAMERA_Y, CAMERA_Z);

	// カメラに位置を反映.
	SetCameraPositionAndTarget_UpVecY(m_pos, VGet(0, 0, 0));
}